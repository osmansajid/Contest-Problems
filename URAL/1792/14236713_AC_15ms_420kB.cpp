#include<bits/stdc++.h>
using namespace std;
int main()
{
    int c1,c2,c3,c4,p1,p2,p3;
    cin>>c1>>c2>>c3>>c4>>p1>>p2>>p3;
    if(p1==(c2+c3+c4)%2 && p2==(c1+c3+c4)%2 && p3==(c1+c2+c4)%2){
        cout<<c1<<" "<<c2<<" "<<c3<<" "<<c4<<" "<<p1<<" "<<p2<<" "<<p3<<endl;
    }
    else{
        if(p1==(c2+1+c3+c4)%2 && p2==(c1+c3+c4)%2 && p3==(c1+c2+1+c4)%2){
            cout<<c1<<" "<<(c2+1)%2<<" "<<c3<<" "<<c4<<" "<<p1<<" "<<p2<<" "<<p3<<endl;
        }
        else if(p1==(c2+c3+1+c4)%2 && p2==(c1+c3+1+c4)%2 && p3==(c1+c2+c4)%2){
            cout<<c1<<" "<<c2<<" "<<(c3+1)%2<<" "<<c4<<" "<<p1<<" "<<p2<<" "<<p3<<endl;
        }
        else if(p1==(c2+c3+c4+1)%2 && p2==(c1+c3+c4+1)%2 && p3==(c1+c2+c4+1)%2){
            cout<<c1<<" "<<c2<<" "<<c3<<" "<<(c4+1)%2<<" "<<p1<<" "<<p2<<" "<<p3<<endl;
        }
        else if(p1==(c2+c3+c4)%2 && p2==(c1+1+c3+c4)%2 && p3==(c1+1+c2+c4)%2){
            cout<<(c1+1)%2<<" "<<c2<<" "<<c3<<" "<<c4<<" "<<p1<<" "<<p2<<" "<<p3<<endl;
        }
        else if(p1!=(c2+c3+c4)%2 && p2==(c1+c3+c4)%2 && p3==(c1+c2+c4)%2){
            cout<<c1<<" "<<c2<<" "<<c3<<" "<<c4<<" "<<(p1+1)%2<<" "<<p2<<" "<<p3<<endl;
        }
        else if(p1==(c2+c3+c4)%2 && p2!=(c1+c3+c4)%2 && p3==(c1+c2+c4)%2){
            cout<<c1<<" "<<c2<<" "<<c3<<" "<<c4<<" "<<p1<<" "<<(p2+1)%2<<" "<<p3<<endl;
        }
        else if(p1==(c2+c3+c4)%2 && p2==(c1+c3+c4)%2 && p3!=(c1+c2+c4)%2){
            cout<<c1<<" "<<c2<<" "<<c3<<" "<<c4<<" "<<p1<<" "<<p2<<" "<<(p3+1)%2<<endl;
        }
    }
}
